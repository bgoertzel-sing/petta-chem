#!/usr/bin/env python3
"""Deterministic exp04 rich-chemistry RAF exploratory run.

This host-side harness mirrors the PeTTa exp04 rule pool, but computes closure,
maximal-RAF pruning, greedy core minimization, and a simple 56-tick catalyzed
soup run rather than accepting hand-written summary atoms.
"""
from __future__ import annotations

import argparse
import json
from dataclasses import asdict, dataclass
from pathlib import Path
from typing import Iterable


FOOD = {"A", "B", "C", "D"}
FOOD_FLOOR = 3


@dataclass(frozen=True)
class Rule:
    rid: str
    kind: str
    a: str
    b: str | None
    product: str
    secondary: str | None
    catalyst: str

    @property
    def reactants(self) -> tuple[str, ...]:
        return (self.a,) if self.b is None else (self.a, self.b)

    @property
    def products(self) -> tuple[str, ...]:
        return (self.product,) if self.secondary is None else (self.product, self.secondary)


RULES = [
    Rule("lAB", "ligation", "A", "B", "AB", None, "ABC"),
    Rule("lBC", "ligation", "B", "C", "BC", None, "ABC"),
    Rule("lCD", "ligation", "C", "D", "CD", None, "BCD"),
    Rule("lAC", "ligation", "A", "C", "AC", None, "ACD"),
    Rule("lABC1", "ligation", "AB", "C", "ABC", None, "AB"),
    Rule("lABC2", "ligation", "A", "BC", "ABC", None, "BC"),
    Rule("lBCD1", "ligation", "BC", "D", "BCD", None, "BC"),
    Rule("lBCD2", "ligation", "B", "CD", "BCD", None, "CD"),
    Rule("cAB", "cleavage", "AB", None, "A", "B", "ABC"),
    Rule("cBC", "cleavage", "BC", None, "B", "C", "ABC"),
    Rule("cCD", "cleavage", "CD", None, "C", "D", "BCD"),
    Rule("cABC1", "cleavage", "ABC", None, "AB", "C", "ABC"),
    Rule("cABC2", "cleavage", "ABC", None, "A", "BC", "ABCstar"),
    Rule("cBCD", "cleavage", "BCD", None, "BC", "D", "BCD"),
    Rule("mAB", "modification", "AB", None, "ABstar", None, "ABC"),
    Rule("mBC", "modification", "BC", None, "BCstar", None, "ABC"),
    Rule("mABC", "modification", "ABC", None, "ABCstar", None, "AB"),
    Rule("mBCD", "modification", "BCD", None, "BCDstar", None, "BC"),
    Rule("mAC", "modification", "AC", None, "ACstar", None, "AC"),
    Rule("mCD", "modification", "CD", None, "CDstar", None, "CD"),
]

STRUCTURAL_CONTAINS = {
    "A": {"A"}, "B": {"B"}, "C": {"C"}, "D": {"D"},
    "AB": {"A", "B"}, "AC": {"A", "C"}, "AD": {"A", "D"},
    "BC": {"B", "C"}, "BD": {"B", "D"}, "CD": {"C", "D"},
    "ABC": {"A", "B", "C", "AB", "BC"},
    "ABD": {"A", "B", "D", "AB", "BD"},
    "ACD": {"A", "C", "D", "AC", "CD"},
    "BCD": {"B", "C", "D", "BC", "CD"},
    "ABstar": {"AB"}, "BCstar": {"BC"}, "ACstar": {"AC"}, "CDstar": {"CD"},
    "ABCstar": {"ABC", "AB", "BC"}, "BCDstar": {"BCD", "BC", "CD"},
}


def is_non_food(mol: str) -> bool:
    return mol not in FOOD and mol != "None"


def template_catalyzes(catalyst: str, rule: Rule, *, require_non_food=True) -> bool:
    """Specificity-filtered template catalysis.

    Accepts if the catalyst contains the non-food product or a non-food
    reactant; weak monomer-only matches are deliberately rejected.
    """
    contained = STRUCTURAL_CONTAINS.get(catalyst, set()) | {catalyst}
    motifs = [rule.product, *rule.reactants]
    if require_non_food:
        motifs = [m for m in motifs if is_non_food(m)]
    return any(m in contained for m in motifs)


def closure_from_food(rules: Iterable[Rule]) -> set[str]:
    closure = set(FOOD)
    changed = True
    while changed:
        changed = False
        for r in rules:
            if all(x in closure for x in r.reactants):
                for p in r.products:
                    if p not in closure:
                        closure.add(p)
                        changed = True
    return closure


def maximal_raf_prune(rules: list[Rule]) -> tuple[list[Rule], list[dict]]:
    active = list(rules)
    trace = []
    for step in range(len(rules) + len(STRUCTURAL_CONTAINS)):
        closure = closure_from_food(active)
        products = {p for r in active for p in r.products}
        next_active = []
        removed = []
        for r in active:
            fg = all(x in closure for x in r.reactants)
            # RAF: catalyst must be available in food closure and generated by the candidate set,
            # so externally supplied catalysts do not make a reaction RA.
            ra = r.catalyst in closure and r.catalyst in products and template_catalyzes(r.catalyst, r)
            if fg and ra:
                next_active.append(r)
            else:
                removed.append({"rule": r.rid, "food_generated": fg, "reflexively_autocatalytic": ra})
        trace.append({"step": step, "size": len(active), "closure_size": len(closure), "removed": removed})
        if [r.rid for r in next_active] == [r.rid for r in active]:
            return active, trace
        active = next_active
    return active, trace


def is_raf(rules: list[Rule]) -> bool:
    if not rules:
        return False
    closure = closure_from_food(rules)
    products = {p for r in rules for p in r.products}
    return all(
        all(x in closure for x in r.reactants)
        and r.catalyst in closure
        and r.catalyst in products
        and template_catalyzes(r.catalyst, r)
        for r in rules
    )


def greedy_minimize(rules: list[Rule]) -> list[Rule]:
    core = list(rules)
    changed = True
    while changed:
        changed = False
        for r in list(core):
            candidate = [x for x in core if x != r]
            if is_raf(candidate):
                core = candidate
                changed = True
                break
    return core


def simulate(rules: list[Rule], ticks: int) -> tuple[dict[str, int], list[dict]]:
    abundance = {m: FOOD_FLOOR for m in FOOD}
    events = []
    for tick in range(ticks):
        for f in FOOD:
            abundance[f] = max(abundance.get(f, 0), FOOD_FLOOR)
        catalyzed = []
        basal = []
        for r in rules:
            enough_reactants = all(abundance.get(x, 0) > 0 for x in r.reactants)
            if not enough_reactants:
                continue
            catalyst_present = abundance.get(r.catalyst, 0) > 0
            if catalyst_present and template_catalyzes(r.catalyst, r):
                catalyzed.append(r)
            else:
                # Origins-of-life RAF dynamics normally assume nonzero basal
                # reaction rates.  Here basal reactions are slow but deterministic:
                # they seed food-generated products until catalytic closure appears.
                basal.append(r)
        if tick % 4 == 0 and basal:
            # Keep a small basal trickle even after the first catalytic loop
            # appears, otherwise the earliest side-loop can monopolize the run.
            applicable = basal
            mode = "basal"
            r = applicable[(tick // 4) % len(applicable)]
        elif catalyzed:
            applicable = catalyzed
            mode = "catalyzed"
            r = applicable[(31 + tick) % len(applicable)]
        else:
            continue
        for x in r.reactants:
            if x not in FOOD:  # food is replenished, but still let rules consume non-food reactants
                abundance[x] = abundance.get(x, 0) - 1
        for p in r.products:
            abundance[p] = abundance.get(p, 0) + 1
        events.append({"tick": tick, "rule": r.rid, "kind": r.kind, "mode": mode})
    return dict(sorted(abundance.items())), events


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--ticks", type=int, default=56)
    ap.add_argument("--out", type=Path, required=True)
    args = ap.parse_args()

    maximal, prune_trace = maximal_raf_prune(RULES)
    core = greedy_minimize(maximal)
    final_abundance, events = simulate(RULES, args.ticks)
    no_bias = maximal_raf_prune([Rule(r.rid, r.kind, r.a, r.b, r.product, r.secondary, "None") for r in RULES])[0]
    metrics = {
        "config": {
            "food": sorted(FOOD),
            "food_floor": FOOD_FLOOR,
            "ticks": args.ticks,
            "rule_count": len(RULES),
            "rule_mix": {"ligation": 8, "cleavage": 6, "modification": 6},
            "catalysis": "specificity-filtered deterministic template matching",
        },
        "rules": [asdict(r) for r in RULES],
        "maximal_raf_rule_ids": [r.rid for r in maximal],
        "maximal_raf_size": len(maximal),
        "minimal_core_rule_ids": [r.rid for r in core],
        "minimal_core_size": len(core),
        "minimal_core_is_raf": is_raf(core),
        "no_catalysis_maximal_raf_size": len(no_bias),
        "prune_trace": prune_trace,
        "simulation": {
            "event_count": len(events),
            "first_12_events": events[:12],
            "final_abundance": final_abundance,
            "food_floor_ok": all(final_abundance.get(f, 0) >= FOOD_FLOOR for f in FOOD),
        },
    }
    args.out.write_text(json.dumps(metrics, indent=2, sort_keys=True) + "\n", encoding="utf-8")
    print(json.dumps({
        "maximal_raf_size": len(maximal),
        "minimal_core_size": len(core),
        "minimal_core_is_raf": is_raf(core),
        "events": len(events),
        "no_catalysis_maximal_raf_size": len(no_bias),
    }, indent=2))


if __name__ == "__main__":
    main()
